//Ejericico 1
function mayusculas (palabras) {
    return palabras.toUpperCase();
}

let nombreYApellido = "karla jimenez";
console.log(mayusculas(nombreYApellido));

//Ejericico 2
function minusculas (frase) {
    return frase.toLowerCase();
}

let oracion = "Hola soy Karla";
console.log(minusculas(oracion));

//Ejericico 3
function resta (numero1, numero2) {
    return numero2 - numero1;
}

let resultadoResta = resta(20, 40);
console.log(resultadoResta);

//Ejericico 4
function division (numero1, numero2) {
    return numero2 / numero1;
}

let resultadoDivision = division(20, 40);
console.log(resultadoDivision);

//Ejericico 5
function multiplicacion (numero1, numero2) {
    return numero2 * numero1;
}

let resultadoMultiplicacion = multiplicacion(20, 40);
